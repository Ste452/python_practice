cars = ['bmw', 'audi', 'toyota, subaru']

print("\nHere is the original list:")
print(cars)

# Alphabetical order
print("\nHere is the sorted list:")
cars.sort()
print(cars)

# Reversed alphabet sorted
print("\nHere is the Reverse alphabetic sorted list:")
cars.sort(reverse=True)
print(cars)

# Reverses the sort of the list
cars.reverse()
print(cars)